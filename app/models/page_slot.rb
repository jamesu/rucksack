class PageSlot < ActiveRecord::Base
  belongs_to :page
  belongs_to :rel_object, :polymorphic => true

  attr_accessible :page, :position, :rel_object

  scope :with_widgets, -> { includes(:rel_object).order('position ASC') }
end
